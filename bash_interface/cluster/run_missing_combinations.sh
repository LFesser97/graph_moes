dataset="${parts[0]}"
layer_type="${parts[1]}"
encoding="${parts[2]}"
skip_str="${parts[3]}"
norm_str="${parts[4]}"
router_type="${parts[5]}"
# Everything after 6th colon is the JSON (join with colons if any were in the JSON)
layer_types_json=""
for ((i=6; i<${#parts[@]}; i++)); do
    if [ $i -eq 6 ]; then
        layer_types_json="${parts[$i]}"
    else
        layer_types_json="${layer_types_json}:${parts[$i]}"
    fi
done

