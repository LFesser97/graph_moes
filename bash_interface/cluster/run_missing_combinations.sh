except:
    # Fallback: assume it's already JSON
    try:
        data = json.loads(sys.stdin.read())
        print(json.dumps(data) if isinstance(data, list) else '[\"GCN\", \"GIN\"]')
    except:
        print('[\"GCN\", \"GIN\"]')
" 2>/dev/null || echo '["GCN", "GIN"]')
